class Casting{
public static void main(String a[]){
	
	double i =10;
	int i1 = (int) i;//explicit casting/narrowing casting 
	 
	 long x = 3477;
	 short x1 = (short) x;
	 
	 short y = 87;
	 byte y1 = (byte) y;
	 
	 float z = 589;
	 int z1 = (int) z;
	 
	 double v = 98478694;
	 long v1 = (long) v;
	 
	 int b =748;
	 short b1 = (short) b;
	
	System.out.println(i1);
	System.out.println(x1);
	System.out.println(y1);
	System.out.println(z1);
	System.out.println(v1);
	System.out.println(b1);
	
}
}